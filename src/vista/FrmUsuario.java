    private void btn_guardar_muebleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardar_muebleActionPerformed
        Usuario usuario = new Usuario();
        Ctrl_Usuario controlUsuario = new Ctrl_Usuario();

        //validamos campos vacios
        if (txt_nombre.getText().isEmpty() || txt_apellido.getText().isEmpty() || txt_usuario.getText().isEmpty() || txt_password.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Complete los campos de color rojo");
            txt_nombre.setBackground(Color.red);
            txt_apellido.setBackground(Color.red);
            txt_usuario.setBackground(Color.red);
            txt_password.setBackground(Color.red);
        } else {
            usuario.setNombre(txt_nombre.getText());
            usuario.setApellido(txt_apellido.getText());
            usuario.setUsuario(txt_usuario.getText());
            usuario.setPassword(txt_password.getText());
            
            controlUsuario.guardar(usuario);
            JOptionPane.showMessageDialog(null, "Registro guardado con exito");
            txt_nombre.setBackground(Color.white);
            txt_apellido.setBackground(Color.white);
            txt_usuario.setBackground(Color.white);
            txt_password.setBackground(Color.white);
            
            txt_nombre.setText("");
            txt_apellido.setText("");
            txt_usuario.setText("");
            txt_password.setText("");
            
        }
    }//GEN-LAST:event_btn_guardar_muebleActionPerformed


