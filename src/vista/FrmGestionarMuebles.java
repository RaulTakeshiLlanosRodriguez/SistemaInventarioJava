    private void CargarTablaUsuarios() {
        Connection con = Conexion.conectar();
        DefaultTableModel model = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        String sql = "select idMueble,CodigoPatrimonial, tipo, unidadMedida, estado, procedencia,ubicacion, observacion from muebles";
        Statement st;
        try {
            st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            FrmGestionarMuebles.jTable_muebles = new JTable(model);
            FrmGestionarMuebles.jScrollPane1.setViewportView(FrmGestionarMuebles.jTable_muebles);

            model.addColumn("N°Orden");//ID
            model.addColumn("Cod Patrimonial");
            model.addColumn("Tipo");
            model.addColumn("Unidad");
            model.addColumn("Estado");
            model.addColumn("Procedencia");
            model.addColumn("Ubicacion");
            model.addColumn("Observacion");

            while (rs.next()) {
                Object fila[] = new Object[8];
                for (int i = 0; i < 8; i++) {
                    fila[i] = rs.getObject(i + 1);
                }
                model.addRow(fila);
            }
            con.close();
        } catch (SQLException e) {
            System.out.println("Error al llenar la tabla mueble: " + e);
        }
        //evento para obtener campo al cual el usuario da click
        //y obtener la interfaz que mostrara la informacion general
        jTable_muebles.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                int fila_point = jTable_muebles.rowAtPoint(e.getPoint());
                int columna_point = 0;

                if (fila_point > -1) {
                    idMueble = (int) model.getValueAt(fila_point, columna_point);
                    EnviarDatosUsuarioSeleccionado(idMueble);//metodo
                }
            }
        });
    }


    /*
     * **************************************************
     * Metodo que envia datos seleccionados
     * **************************************************
     */
    private void EnviarDatosUsuarioSeleccionado(int idMueble) {
        try {
            Connection con = Conexion.conectar();
            PreparedStatement pst = con.prepareStatement(
                    "select nrOrden,CodigoPatrimonial, tipo, unidadMedida, estado, procedencia,ubicacion, observacion from muebles where idMueble='" + idMueble + "'");
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                txt_nroOrden.setText(rs.getString("nrOrden"));
                txt_cod_patrimonial.setText(rs.getString("CodigoPatrimonial"));
                txt_tipo.setText(rs.getString("tipo"));
                txt_unidad.setText(rs.getString("unidadMedida"));
                txt_procedencia.setText(rs.getString("procedencia"));
                txt_ubicacion.setText(rs.getString("ubicacion"));
                txt_observacion.setText(rs.getString("observacion"));
            }
            con.close();
        } catch (SQLException e) {
            System.out.println("Error al seleccionar mueble: " + e);
        }
    }
    
    private DefaultTableModel buscarMueble(String buscar){
        String[] nombreColumna={"Nro Orden","Cod Patrimonial","tipo","unidad","estado","procedencia","ubicacion","observacion"};
        String[] nroCampos = new String[8];
        DefaultTableModel modelo = new DefaultTableModel(null,nombreColumna);
        
        String sql = "select idMueble,CodigoPatrimonial, tipo, unidadMedida, estado, procedencia,ubicacion, observacion from muebles where idMueble like '%"+buscar+"%' or CodigoPatrimonial like '%"+buscar+"%' or tipo like '%"+buscar+"%' or unidadMedida like '%"+buscar+"%' or estado like '%"+buscar+"%' or procedencia like '%"+buscar+"%' or ubicacion like '%"+buscar+"%' or observacion like '%"+buscar+"%'";
        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            
            
            while(rs.next()){
                nroCampos[0] = rs.getString("idMueble");
                nroCampos[1] = rs.getString("CodigoPatrimonial");
                nroCampos[2] = rs.getString("tipo");
                nroCampos[3] = rs.getString("unidadMedida");
                nroCampos[4] = rs.getString("estado");
                nroCampos[5] = rs.getString("procedencia");
                nroCampos[6] = rs.getString("ubicacion");
                nroCampos[7] = rs.getString("observacion");
                modelo.addRow(nroCampos);
            }
        } catch (SQLException e) {
            System.out.println(e);
        }
        return modelo;
    }
    
    private void buscar(String valor){
        DefaultTableModel modelo = buscarMueble(valor);
        jTable_muebles.setModel(modelo);
    }

