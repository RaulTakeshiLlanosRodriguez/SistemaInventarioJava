    private void jButton_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_eliminarActionPerformed
        
        Ctrl_Mueble controlMueble = new Ctrl_Mueble();
        if (idMueble == 0) {
            JOptionPane.showMessageDialog(null, "¡Seleccione un mueble!");
        } else {
            if (!controlMueble.eliminar(idMueble)) {
                JOptionPane.showMessageDialog(null, "¡Mueble Eliminado!");
                this.CargarTablaUsuarios();
                this.Limpiar();
                idMueble = 0;
            } else {
                JOptionPane.showMessageDialog(null, "¡Error al eliminar mueble!");
                this.Limpiar();
            }
        }
        
    }//GEN-LAST:event_jButton_eliminarActionPerformed

    private void jButton_actualizar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_actualizar1ActionPerformed
        Mueble mueble = new Mueble();
        Ctrl_Mueble controlUsuario = new Ctrl_Mueble();

        if (idMueble == 0) {
            JOptionPane.showMessageDialog(null, "¡Seleccione un mueble!");
        } else {
            if (txt_nroOrden.getText().isEmpty() ||txt_cod_patrimonial.getText().equals("")||txt_tipo.getText().equals("")||txt_unidad.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "¡Completa todos los campos!");

            } else {
                mueble.setNrOrden(Integer.parseInt(txt_nroOrden.getText()));
                mueble.setCodPatrimonial(txt_cod_patrimonial.getText());
                mueble.setTipo(txt_tipo.getText());
                mueble.setUnidadMedida(txt_unidad.getText());
                mueble.setEstado(lista_estado.getSelectedItem().toString());
                mueble.setProcedencia(txt_procedencia.getText());
                mueble.setUbicacion(txt_ubicacion.getText());
                mueble.setObservacion(txt_observacion.getText());
                
                if(controlUsuario.actualizar(mueble, idMueble)){
                    JOptionPane.showMessageDialog(null, "¡Actualizacion Exitosa!");
                    this.Limpiar();
                    this.CargarTablaUsuarios();
                    idMueble = 0;
                    
                }else{
                    JOptionPane.showMessageDialog(null, "¡Error al Actualizar usuario!");
                }
            }
        }
    }//GEN-LAST:event_jButton_actualizar1ActionPerformed


