    private void centrarVentana(JInternalFrame miInternalFrame){
        int x = (jDesktopPane_menu.getWidth()/2)-miInternalFrame.getWidth()/2;
        int y = (jDesktopPane_menu.getHeight()/2)-miInternalFrame.getHeight()/2;
        if(miInternalFrame.isShowing()){
            miInternalFrame.setLocation(x, y);
        }else{
            jDesktopPane_menu.add(miInternalFrame);
            miInternalFrame.setLocation(x, y);
            miInternalFrame.show();
        }
    }
